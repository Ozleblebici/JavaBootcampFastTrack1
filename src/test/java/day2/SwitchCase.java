package day2;

public class SwitchCase {


    public static void main(String[] args) {

        int number=100;
        switch (number){

            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Wed");
                break;

            default:
                System.out.println("INVALID DATA");

        }
    }
}
